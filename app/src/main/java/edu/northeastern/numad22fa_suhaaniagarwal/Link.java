package edu.northeastern.numad22fa_suhaaniagarwal;

public class Link {
    private final String link;
    private final String name;

    public Link(String link, String name) {
        this.link = link;
        this.name = name;
    }

    public String getLink() {
        return link;
    }

    public String getName() {
        return name;
    }
}
